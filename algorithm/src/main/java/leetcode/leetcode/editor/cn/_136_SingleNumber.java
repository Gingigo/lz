class _136_SingleNumber {
    public static void main(String[] args) {
        int i = new _136_SingleNumber().singleNumber(new int[]{3, 3, 5});
        System.out.println(i);
    }

    public int singleNumber(int[] nums) {
        int ans = nums[0];
        for (int i = 1; i < nums.length; i++) {
            ans = ans ^ nums[i];
        }
        return ans;
    }
}
